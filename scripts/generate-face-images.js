import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import puppeteer from 'puppeteer';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ASSETS_DIR = path.join(__dirname, '../public/assets');

console.log('Generating OSMD Face Images for Cubes...');

(async () => {
    try {
        const browser = await puppeteer.launch({ headless: 'new', args: ['--no-sandbox', '--disable-setuid-sandbox'] });
        const page = await browser.newPage();
        
        page.on('console', msg => console.log('BROWSER:', msg.text()));

        const osmdJs = fs.readFileSync(path.join(__dirname, '../node_modules/opensheetmusicdisplay/build/opensheetmusicdisplay.min.js'), 'utf8');

        await page.setContent(`
        <!DOCTYPE html>
        <html>
        <head>
            <meta charset="UTF-8">
            <script>${osmdJs}</script>
            <style>body { margin: 0; background: white; }</style>
        </head>
        <body>
            <div id="container" style="position:absolute;top:-9999px;width:512px;"></div>
            <script>
                async function renderNotation(xml) {
                    try {
                        const OSMD = window.opensheetmusicdisplay.OpenSheetMusicDisplay;
                        const osmd = new OSMD('container', {
                            backend: 'canvas',
                            drawTitle: false, drawPartNames: false, drawMeasureNumbers: false, autoResize: false
                        });
                        osmd.EngravingRules.RenderClefsAtBeginningOfStaffline = false;
                        osmd.EngravingRules.RenderTimeSignatures = false;
                        osmd.EngravingRules.RenderKeySignatures = false;
                        osmd.EngravingRules.StaffLineWidth = 2.0;
                        osmd.EngravingRules.StemWidth = 2.5;
                        osmd.EngravingRules.LedgerLineWidth = 2.0;
                        osmd.EngravingRules.BeamWidth = 2.5;
                        osmd.zoom = 3.0;

                        await osmd.load(xml);
                        osmd.render();

                        const canvas = document.querySelector('canvas');
                        if (!canvas) throw new Error('No canvas generated by OSMD');

                        const finalCanvas = document.createElement('canvas');
                        finalCanvas.width = 512; finalCanvas.height = 512;
                        const ctx = finalCanvas.getContext('2d');
                        ctx.fillStyle = 'white';
                        ctx.fillRect(0, 0, 512, 512);

                        const margin = 20;
                        const scaleW = (512 - margin) / canvas.width;
                        const scaleH = (512 - margin) / canvas.height;
                        const scale = Math.min(scaleW, scaleH, 1.0);
                        const drawW = canvas.width * scale;
                        const drawH = canvas.height * scale;
                        const dx = (512 - drawW) / 2;
                        const dy = (512 - drawH) / 2;

                        ctx.drawImage(canvas, 0, 0, canvas.width, canvas.height, dx, dy, drawW, drawH);
                        return finalCanvas.toDataURL('image/png');
                    } catch (err) {
                        return { error: err.message || String(err) };
                    }
                }
                window.renderNotation = renderNotation;
            </script>
        </body>
        </html>
        `);

        const xml = '<?xml version="1.0" encoding="UTF-8"?><!DOCTYPE score-partwise PUBLIC "-//Recordare//DTD MusicXML 3.1 Partwise//EN" "http://www.musicxml.org/dtds/partwise.dtd"><score-partwise version="3.1"><part-list><score-part id="P1"><part-name></part-name></score-part></part-list><part id="P1"><measure number="1"><attributes><divisions>4</divisions><key><fifths>0</fifths></key><time><beats>1</beats><beat-type>4</beat-type></time><clef><sign>percussion</sign><line>2</line></clef></attributes><note><pitch><step>B</step><octave>4</octave></pitch><duration>4</duration><type>quarter</type><stem>down</stem></note></measure></part></score-partwise>';

        const result = await page.evaluate((xmlData) => window.renderNotation(xmlData), xml);

        if (result && result.error) {
            console.error('Failed inside puppeteer:', result.error);
        } else if (result) {
            const base64Data = result.replace(/^data:image\/png;base64,/, '');
            fs.writeFileSync(path.join(ASSETS_DIR, 'face-quarter.png'), base64Data, 'base64');
            console.log('Saved face-quarter.png successfully.');
        } else {
            console.error('No result from script.');
        }
        
        await browser.close();
    } catch (e) {
        console.error('Script failed:', e);
    }
})();
